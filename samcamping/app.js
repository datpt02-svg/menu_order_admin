function looksCorrupted(value) {
  if (typeof value !== "string") return true;
  const markers = [
    "?",
    "\ufffd",
    "\u00e2\u20ac",
    "\u00e1\u00ba",
    "\u00e1\u00bb",
    "\u00c4\u2018",
    "\u00c4\u0090",
  ];
  return markers.some((marker) => value.includes(marker));
}

