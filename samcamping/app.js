function t(value) {
  if (typeof value === "string") return value;
  const localized = value?.[state.locale];
  if (typeof localized === "string" && !isBrokenText(localized)) return localized;
  const english = value?.en;
  if (state.locale !== "vi" && typeof english === "string" && !isBrokenText(english)) return english;
  const vietnamese = value?.vi;
  if (typeof vietnamese === "string" && !isBrokenText(vietnamese)) return vietnamese;
  return english || vietnamese || "";
}

function isBrokenText(value) {
  const markers = [
    "\ufffd",
    "\u00e2\u20ac",
    "\u00e2\u20ac\u2122",
    "\u00e2\u20ac\u0153",
    "\u00e2\u20ac\ufffd",
    "\u00e2\u20ac\u201c",
    "\u00e2\u20ac\u201d",
    "\u00e1\u00ba",
    "\u00e1\u00bb",
    "\u00c4\u2018",
    "\u00c4\u0090",
  ];
  return markers.some((marker) => value.includes(marker));
}

function escapeHtml(value) {
  return String(value ?? "")
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#039;");
}

function looksLikeServerLog(value) {
  const text = String(value || "");
  return text.includes("[booking-api]") || text.includes("OPTIONS /api/bookings") || text.includes("POST /api/bookings") || text.includes("GET /api/bookings");
}

