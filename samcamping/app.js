document.addEventListener("click", (event) => {
  const target = event.target;
  if (!(target instanceof HTMLElement)) return;

  const categoryButton = target.closest("[data-category]");
  const detailButton = target.closest("[data-detail]");
  const localeButton = target.closest("[data-locale]");
  const bookingServiceButton = target.closest("[data-booking-service]");
  const bookingBackButton = target.closest("[data-booking-back]");
  const bookingNextButton = target.closest("[data-booking-next]");
  const bookingSubmitButton = target.closest("[data-booking-submit]");
  const bookingCloseButton = target.closest("[data-booking-close]");
  const historyPaymentLink = target.closest("[data-history-payment]");
  const historyCallButton = target.closest("[data-history-call]");
  const historyResubmitButton = target.closest("[data-history-resubmit]");
  const historyMoreButton = target.closest("[data-history-more]");
  const bookingCancelButton = target.closest("[data-booking-cancel]");
  const historyCancelButton = target.closest("[data-history-cancel]");
  const waiterLocationButton = target.closest("[data-waiter-location]");
  const waiterNeedButton = target.closest("[data-waiter-need]");
  const waiterSubmitButton = target.closest("[data-waiter-submit]");
  const waiterResetButton = target.closest("[data-waiter-reset]");
  const waiterResolveButton = target.closest("[data-waiter-resolve]");

  if (categoryButton instanceof HTMLElement) {
    const category = categoryButton.dataset.category;
    if (category) {
      state.activeSection = category;
      renderCategories();
      document.getElementById(category)?.scrollIntoView({
        behavior: "smooth",
        block: "start",
      });
    }
  }

  if (detailButton instanceof HTMLElement) {
    const detail = detailButton.dataset.detail;
    if (detail) {
      state.selectedItemId = detail;
      renderProductScreen();
    }
  }

  if (localeButton instanceof HTMLElement) {
    const locale = localeButton.dataset.locale;
    if (locale && locales[locale]) {
      state.locale = locale;
      Promise.all([
        loadBookingServices().catch(() => {
          state.bookingServices = [];
        }),
        loadBookingConfig().catch(() => {
          state.bookingConfig = { ...DEFAULT_BOOKING_CONFIG };
        }),
      ]).finally(() => {
        renderApp();
        languageModal.close();
      });
    }
  }

  if (bookingServiceButton instanceof HTMLElement) {
    state.bookingDraft.serviceId = bookingServiceButton.dataset.bookingService || "";
    clearBookingFieldError("serviceId");
    state.bookingError = "";
    state.bookingStep = "form";
    renderBookingFlow();
  }

  if (bookingBackButton instanceof HTMLElement) {
    state.bookingError = "";
    state.bookingStep = bookingBackButton.dataset.bookingBack || "service";
    renderBookingFlow();
  }

  if (bookingNextButton instanceof HTMLElement) {
    moveToBookingStep(bookingNextButton.dataset.bookingNext || state.bookingStep);
  }

  if (bookingSubmitButton instanceof HTMLElement) {
    if (state.bookingStep === "deposit") {
      if (!hasBookingReceipt()) {
        state.bookingError = getBookingReceiptRequiredError();
        renderBookingFlow();
        return;
      }

      state.bookingError = "";
      logBookingTrace("click-submit-deposit", {
        buttonText: bookingSubmitButton.textContent?.trim() || "",
      });
      bookingSubmitButton.setAttribute("disabled", "disabled");
      submitBookingDraft()
        .then(() => {
          setBookingPendingState();
          state.bookingStep = "pending";
          renderBookingFlow();
          renderHistoryFlow();
        })
        .catch((error) => {
          if (isActiveBookingConflictError(error)) {
            const conflictBooking = error.booking || null;
            if (conflictBooking && conflictBooking.depositReviewStatus !== "rejected") {
              const latestBooking = syncLatestBookingRecord(conflictBooking);
              if (latestBooking && openBookingFlowForLatestBooking(latestBooking)) {
                state.bookingError = getActiveBookingConflictMessage();
              } else {
                state.bookingError = getBookingSubmitErrorMessage();
              }
            } else {
              state.bookingError = getBookingSubmitErrorMessage();
            }
          } else {
            state.bookingError = getBookingSubmitErrorMessage();
          }
          bookingSubmitButton.removeAttribute("disabled");
          renderHistoryFlow();
          renderBookingFlow();
        });
      return;
    }

    if (!validateBookingDraft()) {
      renderBookingFlow();
      return;
    }

    state.bookingError = "";
    state.bookingDraft.code = generateBookingCode();
    state.bookingStep = "deposit";
    persistLatestBooking();
    renderBookingFlow();
  }

  if (historyCallButton instanceof HTMLElement) {
    handleHistoryCall(historyCallButton.dataset.historyCall || "");
    return;
  }

  if (historyResubmitButton instanceof HTMLElement) {
    const latestBooking = getLatestBooking();
    if (!latestBooking) return;
    if (!canResubmitHistoryReceipt(latestBooking)) {
      setHistoryBookingError(latestBooking, getBookingReceiptRequiredError());
      renderHistoryFlow();
      return;
    }

    historyResubmitButton.setAttribute("disabled", "disabled");
    resubmitBookingReceipt()
      .then(() => {
        const nextBooking = getLatestBooking();
        if (nextBooking) {
          syncBookingDraftFromHistory(nextBooking);
          state.bookingStep = "pending";
        }
        renderHistoryFlow();
        renderBookingFlow();
      })
      .catch(() => {
        const currentBooking = getLatestBooking();
        if (currentBooking) {
          setHistoryBookingError(currentBooking, getBookingResubmitErrorMessage());
        }
        historyResubmitButton.removeAttribute("disabled");
        renderHistoryFlow();
      });
  }

  if (historyMoreButton instanceof HTMLElement) {
    historyMoreButton.setAttribute("disabled", "disabled");
    state.bookingHistoryVisibleCount += 5;
    if (state.bookingHistoryVisibleCount > state.bookingHistory.length && state.bookingHistoryNextCursor) {
      fetchApprovedBookingHistory({ append: true })
        .finally(() => {
          renderHistoryFlow();
        });
      return;
    }
    renderHistoryFlow();
    return;
  }

  if (bookingCancelButton instanceof HTMLElement || historyCancelButton instanceof HTMLElement) {
    const trigger = bookingCancelButton instanceof HTMLElement ? bookingCancelButton : historyCancelButton;
    trigger?.setAttribute("disabled", "disabled");
    cancelLatestBooking()
      .then((result) => {
        if (maybeHandleCancelledBookingOutcome(result)) {
          if (isDeletedBookingOutcome(result)) {
            setBookingError(getBookingDeletedMessage());
          } else if (isCancelledBookingOutcome(result)) {
            setBookingError(getBookingCancelledAfterConfirmMessage());
          }
          renderBookingFlow();
          renderHistoryFlow();
          return;
        }
        refreshBookingUiAfterCancel();
      })
      .catch(() => {
        setBookingError(getBookingCancelErrorMessage());
        trigger?.removeAttribute("disabled");
        renderBookingFlow();
        renderHistoryFlow();
      });
    return;
  }

  if (bookingCloseButton instanceof HTMLElement) {
    bookingModal.close();
    resetBookingDraft();
    renderBookingFlow();
  }

  if (historyPaymentLink instanceof HTMLElement) {
    const latestBooking = getLatestBooking();
    if (!latestBooking) return;
    state.bookingDraft = {
      ...state.bookingDraft,
      ...latestBooking,
      serviceId: latestBooking.serviceId || state.bookingDraft.serviceId || "",
      customerName: latestBooking.customerName || state.bookingDraft.customerName || "",
      customerPhone: latestBooking.customerPhone || state.bookingDraft.customerPhone || "",
      bookingDate: latestBooking.bookingDate || state.bookingDraft.bookingDate || "",
      bookingTime: latestBooking.bookingTime || state.bookingDraft.bookingTime || "",
      guestCount: latestBooking.guestCount || state.bookingDraft.guestCount || "2",
      note: latestBooking.note || state.bookingDraft.note || "",
      receiptName: latestBooking.receiptName || state.bookingDraft.receiptName || "",
      receiptPath: latestBooking.receiptPath || state.bookingDraft.receiptPath || "",
      code: latestBooking.code || state.bookingDraft.code || "",
      status: latestBooking.status || state.bookingDraft.status || "submitted",
      depositReviewNote: latestBooking.depositReviewNote || state.bookingDraft.depositReviewNote || "",
    };
    state.bookingStep = "deposit";
    historyModal.close();
    renderBookingFlow();
    bookingModal.showModal();
  }

  if (waiterLocationButton instanceof HTMLElement) {
    state.waiterDraft.location = waiterLocationButton.dataset.waiterLocation || "";
    state.waiterDraft.zoneSlug = waiterLocationButton.dataset.waiterZoneSlug || "";
    state.waiterError = "";
    renderWaiterFlow();
  }

  if (waiterNeedButton instanceof HTMLElement) {
    state.waiterDraft.need = waiterNeedButton.dataset.waiterNeed || "call-staff";
    if (state.waiterDraft.need !== "add-supplies") state.waiterDraft.groceryItems = [];
    renderWaiterFlow();
  }

  if (waiterSubmitButton instanceof HTMLElement) {
    if (!state.waiterDraft.location.trim()) {
      state.waiterError = getWaiterLocale().locationRequired;
      renderWaiterFlow();
      return;
    }
    state.waiterError = "";
    waiterSubmitButton.setAttribute("disabled", "disabled");
    submitWaiterDraft()
      .then(() => {
        state.waiterStep = "sent";
        renderWaiterFlow();
      })
      .catch(() => {
        waiterSubmitButton.removeAttribute("disabled");
      });
  }

  if (waiterResolveButton instanceof HTMLElement) {
    state.waiterDraft.status = "resolved";
    persistLatestWaiterRequest();
    state.waiterStep = "resolved";
    renderWaiterFlow();
  }

  if (waiterResetButton instanceof HTMLElement) {
    resetWaiterDraft();
    renderWaiterFlow();
  }

  if (target.closest("#product-back")) {
    state.selectedItemId = null;
    renderProductScreen();
  }
});

