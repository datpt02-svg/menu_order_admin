function getBookingFieldErrors() {
  const copy = uiCopy[state.locale] || uiCopy.en;
  const errors = {};
  const customerName = state.bookingDraft.customerName.trim();
  const customerPhone = state.bookingDraft.customerPhone.trim();
  const normalizedPhone = customerPhone.replace(/[().\s-]+/g, "");
  const guestCountRaw = String(state.bookingDraft.guestCount || "").trim();
  const guestCount = Number(guestCountRaw);
  const bookingDate = String(state.bookingDraft.bookingDate || "").trim();
  const bookingTime = String(state.bookingDraft.bookingTime || "").trim();
  const note = state.bookingDraft.note.trim();

  if (!state.bookingDraft.serviceId) {
    errors.serviceId = copy.bookingServiceDesc;
  }

  if (!customerName) {
    errors.customerName = `${copy.fieldCustomerName} chưa được nhập.`;
  } else if (customerName.length < 2) {
    errors.customerName = `${copy.fieldCustomerName} phải có ít nhất 2 ký tự.`;
  }

  if (!customerPhone) {
    errors.customerPhone = `${copy.fieldPhone} chưa được nhập.`;
  } else if (!/^[0-9+().\s-]+$/.test(customerPhone) || normalizedPhone.length < 9 || normalizedPhone.length > 15) {
    errors.customerPhone = `${copy.fieldPhone} không hợp lệ.`;
  }

  if (!guestCountRaw) {
    errors.guestCount = `${copy.fieldAdults} chưa được nhập.`;
  } else if (!Number.isInteger(guestCount) || guestCount < 1) {
    errors.guestCount = `${copy.fieldAdults} phải lớn hơn hoặc bằng 1.`;
  }

  if (!bookingDate) {
    errors.bookingDate = `${copy.fieldDate} chưa được chọn.`;
  } else if (Number.isNaN(Date.parse(`${bookingDate}T00:00:00`))) {
    errors.bookingDate = `${copy.fieldDate} không hợp lệ.`;
  }

  if (!bookingTime) {
    errors.bookingTime = `${copy.fieldTime} chưa được chọn.`;
  } else if (!/^([01]\d|2[0-3]):([0-5]\d)$/.test(bookingTime)) {
    errors.bookingTime = `${copy.fieldTime} chưa hợp lệ.`;
  }
  if (note.length > 1000) {
    errors.note = `${copy.fieldNote} quá dài.`;
  }

  return errors;
}

